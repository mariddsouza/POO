public class Cal{
    public int sum(int Numero1, int Numero2) { return Numero1 + Numero2;}
    
    public int sub(int Numero1, int Numero2) { return Numero1 - Numero2;}
    
    public int mult(int Numero1, int Numero2) { return Numero1 * Numero2;}
    
    public float div(float Numero1, float Numero2) { return Numero1 / Numero2;}
}
